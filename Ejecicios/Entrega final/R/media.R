#' @title Media de un vector de numeros
#' @description Se hace la media a trav√©s de un vector de numeros. Calculado por (a1+a2+...+an)/n
#' @param vector Vector que contiene los numeros asociados a la media que se quiera calcular.
#' @return resultado de la media.
#' @export media
#' @author PersonalCastro
#' @examples
#' media(c(1,2,3,4,5))
#' media(c(1,2))
#' media(c(-1,2,3,-4,))
#' media(c(1))

media=function(vector){

  i=0;
  sumaTotal = 0;
  for (number in vector) {
      sumaTotal = sumaTotal + number;
      i=i+1;
    }
  resultado = sumaTotal/i;
  resultado;
}
